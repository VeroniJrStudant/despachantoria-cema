  // Fun√ß√µes OAuth2
  function iniciarLoginOAuth2() {
    const clientId = document.getElementById("clientId").value.trim();
    
    if (!clientId) {
      mostrarStatus("Por favor, configure o Client ID OAuth2 primeiro", "error");
      return;
    }
    
    GOOGLE_OAUTH_CONFIG.clientId = clientId;
    
    // Gerar state para seguran√ßa
    const state = Math.random().toString(36).substring(2, 15);
    sessionStorage.setItem('oauth_state', state);
    
    // Construir URL de autoriza√ß√£o
    const authUrl = new URL('https://accounts.google.com/o/oauth2/v2/auth');
    authUrl.searchParams.set('client_id', clientId);
    authUrl.searchParams.set('redirect_uri', GOOGLE_OAUTH_CONFIG.redirectUri);
    authUrl.searchParams.set('scope', GOOGLE_OAUTH_CONFIG.scope);
    authUrl.searchParams.set('response_type', 'token');
    authUrl.searchParams.set('state', state);
    authUrl.searchParams.set('prompt', 'consent');
    
    // Redirecionar para autoriza√ß√£o
    window.location.href = authUrl.toString();
  }

  function processarCallbackOAuth2() {
    const hash = window.location.hash.substring(1);
    const params = new URLSearchParams(hash);
    
    const accessTokenParam = params.get('access_token');
    const stateParam = params.get('state');
    const errorParam = params.get('error');
    
    const savedState = sessionStorage.getItem('oauth_state');
    
    if (errorParam) {
      mostrarStatus(`Erro na autentica√ß√£o: ${errorParam}`, "error");
      return;
    }
    
    if (stateParam !== savedState) {
      mostrarStatus("Erro de seguran√ßa na autentica√ß√£o", "error");
      return;
    }
    
    if (accessTokenParam) {
      accessToken = accessTokenParam;
      sessionStorage.setItem('oauth_access_token', accessToken);
      sessionStorage.removeItem('oauth_state');
      
      // Limpar hash da URL
      window.history.replaceState({}, document.title, window.location.pathname);
      
      // Obter informa√ß√µes do usu√°rio
      obterPerfilUsuario();
      
      const mensagemSucesso = `Login realizado com sucesso!\n\nToken de acesso obtido\nSess√£o iniciada\nRedirecionando...`;
      mostrarStatus(mensagemSucesso, "success");
    }
  }

